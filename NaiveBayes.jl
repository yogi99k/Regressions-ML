# Define a function to compute Gaussian probability with Laplace smoothing
function gaussian_probability(x, μ, σ)
    return pdf(Normal(μ, σ + 1e-5), x)
end

# ╔═╡ 45d813ed-c5e6-4685-8618-9624750f322c
# Train Naive Bayes classifier with Laplace smoothing
function train_naive_bayes(train_x, train_y)
    # Compute mean and standard deviation for each pixel and class
    μ = zeros(10, 28*28)
    σ = zeros(10, 28*28)
    for i in 0:9
        idx = findall(train_y .== i)
        μ[i+1, :] .= mean(train_x[:, idx], dims=2)[:]
        σ[i+1, :] .= std(train_x[:, idx], dims=2)[:]
    end
    return μ, σ
end

# ╔═╡ 8c56c206-8591-4a6d-ad2a-4556fb2cc68e
# Predict using Naive Bayes classifier
function predict_naive_bayes(x, μ, σ)
    scores = zeros(10)
    for i in 1:10
        score = 0.0
        for j in 1:28*28
            score += log(gaussian_probability(x[j], μ[i, j], σ[i, j]))
        end
        scores[i] = score
    end
    return argmax(scores) - 1
end

