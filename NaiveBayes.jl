begin
using CSV
using DataFrames

function entropy(data)
   classes = unique(data[!, :class])
   n = length(data[!, :class])
   ent = 0.0
   for c in classes
       p = sum(data[!, :class] .== c) / n
       if p != 0
           ent -= p * log2(p)
       end
   end
   return ent
end

function information_gain(data, attribute)
   n = length(data[!, :class])
   attr_values = unique(skipmissing(data[!, attribute]))
   gain = entropy(data)
   splits = Dict{String, Dict{Any, Int}}()  # Dictionary to store counts for each class in each subset
   for val in attr_values
       subset = data[.!ismissing.(data[!, :class]) .& .!ismissing.(data[!, attribute]) .& (data[!, attribute] .== val), :]
       if !isempty(subset)
           gain -= (length(subset[!, :class]) / n) * entropy(subset)
           # Count entries for each class in the subset
           count_dict = combine(groupby(subset, :class), nrow => :count)
           splits[string(val)] = Dict(zip(count_dict[!, :class], count_dict[!, :count]))
           println("Value: $val, Entries: $(nrow(subset))")
       end
   end
   return gain, splits
end

